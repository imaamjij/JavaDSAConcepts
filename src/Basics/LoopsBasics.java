package Basics;

public class LoopsBasics {
    public static void main(String[] args) {
        //while loop
        int count=1;
        while(count<=5){
            System.out.println(count);
            count++;
        }
        // for loop
        for(int i = 1; i <= 5; i++){
            System.out.println(i);
        }
    }
}
